import Foundation

extension Character {
    
    /// UtilityFramework: A new Line character
    static var newLineCharacter: Character {
        return "\n"
    }
    
    /// UtilityFramework: A white space character
    static var whiteSpaceCharacter: Character {
        return " "
    }
}
